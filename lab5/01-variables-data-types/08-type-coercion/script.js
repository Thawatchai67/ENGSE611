// ตัวอย่างที่ 1: การบวก String กับ Number
console.log("5" + 2); 
console.log(5 + "2"); 

// ตัวอย่างที่ 2: การลบ String กับ Number
console.log("5" - 2);
console.log("10" * 2); 
console.log("8" / "2"); 

// ตัวอย่างที่ 3: Boolean กับ Number
console.log(true + 1); 
console.log(false + 1); 
console.log(true + true); 

// ตัวอย่างที่ 4: null และ undefined
console.log(null + 5); 
console.log(undefined + 5); 

// ตัวอย่างที่ 5: การเปรียบเทียบที่ใช้ Type Coercion
console.log("5" == 5); 
console.log("5" === 5); 

// ตัวอย่างที่ 6: Object กับ Primitive Type
console.log([1, 2] + [3, 4]); 
console.log([] + {}); 
console.log({} + []); 

// ตัวอย่างที่ 7: Unary + Operator
console.log(+"42"); 
console.log(+true); 
console.log(+false); 
console.log(+null); 
console.log(+undefined); 